export function isLocale(value: string): value is Locale {
  return locales.includes(value as Locale);
}

export function translateStatus(locale: Locale, status: keyof Dictionary["statuses"]) {
  return dictionaries[locale].statuses[status];
}

export function translatePriority(locale: Locale, priority: keyof Dictionary["priorities"]) {
  return dictionaries[locale].priorities[priority];
}

export function translateRole(locale: Locale, role: keyof Dictionary["roles"]) {
  return dictionaries[locale].roles[role];
}

export function translateArea(locale: Locale, area: string) {
  const key = areaKeyMap[area];
  return key ? dictionaries[locale].areas[key] : area;
}

export function translateLoginError(locale: Locale, error?: string) {
  const t = dictionaries[locale].auth;
  if (error === "invalid_credentials") return t.invalidCredentials;
  if (error === "invalid_input") return t.invalidInput;
  if (error === "rate_limited") return t.rateLimited;
  return t.unknownError;
}
